let mayorTeffa = parseInt (prompt ('¿Qué edad tiene Teffa?'));
let menorDiego = parseInt (prompt ('¿Qué edad tiene Diego?'));

if (mayorTeffa > menorDiego) {
  alert(`Teffa tiene ${mayorTeffa} por lo tanto es mayor que Diego`);
}else{
  alert(`Teffa no es mayor que Diego porque Diego tiene ${menorDiego} años` );
}
